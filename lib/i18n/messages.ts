export const MESSAGES: Record<Lang, Dict> = { fr, en, es, it, de, pt };

// Recupere une chaine traduite (fallback FR puis clé brute) avec interpolation {var}.
export function translate(
  lang: Lang,
  key: string,
  vars?: Record<string, string | number>
): string {
  const dict = MESSAGES[lang] || MESSAGES.fr;
  let s = dict[key] ?? MESSAGES.fr[key] ?? key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(vars[k]));
    }
  }
  return s;
}

